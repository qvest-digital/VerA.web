package de.tarent.commons.dataaccess;

import java.io.IOException;

/**
 * This {@link RuntimeException} will be thrown by all internal
 * {@link Exception exceptions}. Particularly unexpected backend errors like
 * {@link IOException} etc.
 * 
 * @author Christoph Jerolimov, tarent GmbH
 * @author Timo Pick, tarent GmbH
 */
public class DataAccessException extends RuntimeException {
	private static final long serialVersionUID = -7136795267548290343L;

	public DataAccessException() {
		super();
	}

	public DataAccessException(String message) {
		super(message);
	}

	public DataAccessException(String message, Throwable cause) {
		super(message, cause);
	}

	public DataAccessException(Throwable cause) {
		super(cause);
	}
}
